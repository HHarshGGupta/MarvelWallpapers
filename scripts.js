function filterWallpapers(character) {
  const gallery = document.getElementById('wallpaper-gallery');
  gallery.innerHTML = '';

  const allWallpapers = character === 'all'
    ? Object.values(wallpapers).flat()
    : wallpapers[character];

  allWallpapers.forEach(src => {
    const wrapper = document.createElement('div');
    wrapper.className = 'wallpaper';
    wrapper.innerHTML = `
      <img src="${src}" alt="Wallpaper" />
      <a class="download-btn" href="${src}" download>Download</a>
    `;
    gallery.appendChild(wrapper);
  });
}

